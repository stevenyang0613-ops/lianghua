# 缓存
_name_map = None


def _load_stock_names() -> dict:
    """加载股票名称映射 (首次调用时缓存)"""
    global _name_map
    if _name_map is not None:
        return _name_map
    
    try:
        import akshare as ak
        import tqdm as _tq
        _orig = _tq.tqdm
        _tq.tqdm = lambda *a, **kw: _orig(*a, **{'disable': True, **kw})
        
        df = ak.stock_info_a_code_name()
        _name_map = {}
        for _, r in df.iterrows():
            _name_map[str(r["code"]).strip().zfill(6)] = str(r["name"]).strip()
        logger.info(f"  股票名称加载: {len(_name_map)} 只")
        return _name_map
    except Exception as e:
        logger.warning(f"  股票名称加载失败: {e}")
        _name_map = {}
        return _name_map


def get_industry(code: str) -> str:
    """根据股票名称和代码推断行业"""
    name = None
    
    # 先从名称映射查找
    names = _load_stock_names()
    if names:
        name = names.get(code)
    
    # 关键字匹配
    if name:
        for keyword, industry in _KEYWORD_MAP:
            if keyword in name:
                return industry
    
    # 代码前缀兜底
    for prefix, industry in _PREFIX_MAP:
        if code.startswith(prefix):
            return industry
    
    return "其他"